CREATE TABLE public.metrics_rooms (
	id uuid NOT NULL DEFAULT gen_random_uuid(),
	room_id uuid NOT NULL,
	contact_id uuid NULL,
	channel_integration_id uuid NOT NULL,
	channel_name varchar NOT NULL,
	organization_id uuid NOT NULL,
	started_at timestamp NULL,
	finished_at timestamp NULL,
	finished_by uuid NULL,
	finished_type varchar NULL,
	first_assigned_by uuid NULL,
	first_assigned_type varchar NULL,
	first_assignee_by uuid NULL,
	first_assigned_at timestamp NULL,
	first_response_by uuid NULL,
	first_response_at timestamp NULL,
	first_response_time int8 NULL DEFAULT 0,
	resolution_time int8 NULL DEFAULT 0,
	wait_time int8 NULL DEFAULT 0,
	response_time int8 NULL DEFAULT 0,
	messages_count int4 NULL DEFAULT 0,
	created_at timestamp(6) NOT NULL,
	updated_at timestamp(6) NOT NULL,
	CONSTRAINT metrics_rooms_pkey PRIMARY KEY (id)
);

